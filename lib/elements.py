from .utils import SlotPickleMixin, is_integer

class Element(SlotPickleMixin):

    __slots__ = 'symbol', 'number', 'period', 'group', 'valence', \
                 'mass', 'atomic_radius', 'ionic_radius', 'covalent_radius', 'vdw_radius', \
                 'screening_radius', 'hardness', 'electron_affinity', 'electron_negativity', \
                 'ionization_potentials', 'configuration', 'isotopes', 'name'

    def __init__(self, symbol, number, period, group, valence, mass, \
                 ar, ir, cr, vr, sr, hard, ea, en, ip, conf, iso, name):
        SlotPickleMixin.__init__(self)
        self.symbol = symbol
        self.number = number
        self.period = period
        self.group = group
        self.valence = valence
        self.mass = mass
        self.atomic_radius = ar
        self.ionic_radius = ir
        self.covalent_radius = cr
        self.vdw_radius = vr
        self.screening_radius = sr
        self.hardness = hard
        self.electron_affinity = ea
        self.electron_negativity = en
        self.ionization_potentials = ip
        self.configuration = conf
        self.isotopes = iso
        self.name = name

    def __repr__(self): return "<Element(%d)>" % self.number

__atomic_symbols = []
__atomic_numbers = []
__atomic_pse = []
__elements = []

def index_by_number(no):
    assert is_integer(no) and no > 0 and no < len(__atomic_numbers)
    return no

def index_by_symbol(sym):
    assert isinstance(sym, str) and len(sym) <= 2 and sym.isalpha()
    sym = sym.capitalize()
    if sym in ("X", "Xx"): return 0
    else: return __atomic_symbols.index(sym)

def index_by_pse(pse):
    assert isinstance(pse, tuple) and len(pse) == 2 \
        and is_integer(pse[0]) and is_integer(pse[1]) \
        and pse[0] < 12 and pse[1] < 8
    return __atomic_pse.index(pse)

def index_by_name(name):
    assert len(name) > 0
    if len(name) > 1:
        for i in range(len(name)):
            if name[i].isalpha(): break
        name = name[i:]
        for i in range(len(name)):
            if not name[i].isalpha(): break
        if i+1 < len(name):
            name = name[:i]
    if len(name) >= 2:
        try:
            if name[1].isupper():
                return index_by_symbol(name[0])
            else:
                return index_by_symbol(name[:2])
        except:
            return index_by_symbol(name[0])
    else:
        return index_by_symbol(name)


    for c in name:
        if len(name) == 2 or not c.isalpha(): break
        name += c
    while len(name) > 0:
        try:
            return index_by_symbol(name)
        except ValueError:
            name = name[0]
    return 0

def __get_element_helper(sym_no_pse):
    index = -1
    if is_integer(sym_no_pse):
        index = index_by_number(sym_no_pse)
    elif isinstance(sym_no_pse, str):
        index = index_by_name(sym_no_pse)
    elif isinstance(sym_no_pse, tuple):
        index = index_by_pse(sym_no_pse)
    return index


def get_index(*args, **kargs):
    assert len(args)+len(kargs) == 1
    if len(args) == 1:
        return __get_element_helper(args[0])
    elif len(kargs) == 1:
        switcher = {
            'Z' : index_by_number,
            'no' : index_by_number,
            'name' : index_by_name,
            'sym' : index_by_symbol,
            'symbol' : index_by_symbol,
            'pse' : index_by_pse,
            }
        kk = kargs.items()
        return switcher.get(kk[0][0])(kk[0][1])


def get_element(*args, **kargs):
    assert len(args)+len(kargs) == 1
    return __elements[get_index(*args, **kargs)]

def by_symbol(sym):
    return __elements[index_by_symbol(sym)]

def by_number(sym):
    return __elements[index_by_number(sym)]

def by_index(ind):
    assert is_integer(ind) and len(__elements) > ind
    return __elements[ind]

def element_property(ind, *props):
    if len(props) > 1:
        return (element_property(ind,  p) for p in props)
    else:
        assert hasattr(__elements[0], props[0]), "No property %s in Element" % props[0]
        if is_integer(ind):
            assert len(__elements) > ind
            return getattr(__elements[ind], props[0])
        elif isinstance(ind, Element):
            return getattr(ind, props[0])
        else:
            return None


def get_elements():
    return __elements

